package com.example.kotlin.chapter8

//函数编程的一些基本特点包括:
//函数是"第一等公民"。
//闭包（Closure）和高阶函数（Higher Order Function）。
//Lambda演算与函数柯里化（Currying）。
//懒惰计算（lazy evaluation）。
//使用递归作为控制流程的机制。
//引用透明性。
//没有副作用。(所谓"副作用"（side effect），指的是函数内部与外部互动（最典型的情况，就是修改全局变量的值），产生运算以外的其他结果。
//函数式编程强调没有"副作用"，意味着函数要保持独立，所有功能就是返回一个新的值，没有其他行为，尤其是不得修改外部变量的值)